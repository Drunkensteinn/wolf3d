#include "libft.h"

void		ft_putstr(char const *s)
{
	size_t	i;
	char	*str;

	if (s == NULL)
		return ;
	i = 0;
	str = (char *)s;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}
